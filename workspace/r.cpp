#include <iostream>
using namespace std;

int main() {
    cout << "This is a simple 'threesome' problem example.\n";
    
    int x = 5;
    int y = 10;
    int z = x + y; // Simple sum of two numbers
    
    cout << "The sum of x and y (x=" << x << ", y=" << y << ") is: " << z << endl;
    
    return 0;
}